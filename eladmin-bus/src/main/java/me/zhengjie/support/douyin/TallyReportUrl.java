package me.zhengjie.support.douyin;

public class TallyReportUrl {
    /**
     * 理货报告的url链接
     */
    private String url;

    /**
     * 理货报告附件名称
     */
    private String name;

    public String getUrl() {
        return url;
    }

    public void setUrl(String url) {
        this.url = url;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }
}
