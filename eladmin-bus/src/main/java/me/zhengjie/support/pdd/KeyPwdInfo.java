package me.zhengjie.support.pdd;

public class KeyPwdInfo {
    /**
     * 清关抬头用户名
     */
    private String wmsUser;
    /**
     * 清关抬头秘钥
     */
    private String wmsPwd;

    public String getWmsUser() {
        return wmsUser;
    }

    public void setWmsUser(String wmsUser) {
        this.wmsUser = wmsUser;
    }

    public String getWmsPwd() {
        return wmsPwd;
    }

    public void setWmsPwd(String wmsPwd) {
        this.wmsPwd = wmsPwd;
    }
}
